Complex Complex::operator+(const Complex& other) const {
    Complex result;
    result.setReal(getReal() + other.getReal());
    result.setImag(getImag() + other.getImag());
    return result;
}
//========================================================
// subtraction operators
//========================================================
Complex Complex::operator-(const Complex& other) const {
    Complex result;
    result.setReal(getReal() - other.getReal());
    result.setImag(getImag() - other.getImag());
    return result;
}
