Complex::Complex() {
    setReal(0);
    setImag(0);
}
//========================================================
// copy constructor
//========================================================
Complex::Complex(const Complex& other) {
    setReal(other.getReal());
    setImag(other.getImag());
}
//========================================================
// Complex(a,b) constructor
//========================================================
Complex::Complex(double real, double imag) {
    setReal(real);
    setImag(imag);
}
